#Initialize the Variables
letter1 = "m"
letter2 = "u"
letter3 = "y"

#Initialize Vowels 
vowels = "aeiou"
#Initialize Y 
y = "y"

# Letter1 Check for Vowels 
if letter1 in vowels:
    print(f"{letter1} is a vowel")
elif letter1 in y:
    print(f"{letter1} is y")
else:
   print(f"{letter1} is consonant") 

